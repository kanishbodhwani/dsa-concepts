public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 342.795843f;
        System.out.printf("Formatted string is: %.2f", a);   // Print for formatted string
        // % is a placeholder
        System.out.printf("Hello my name is %s and I am %s", "Kanish", "Cool");

    }


}
